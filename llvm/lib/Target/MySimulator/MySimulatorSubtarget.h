#ifndef __LLVM_LIB_TARGET_MY_SIMULATOR_MY_SIMULATOR_SUBTARGET_H__
#define __LLVM_LIB_TARGET_MY_SIMULATOR_MY_SIMULATOR_SUBTARGET_H__

#include "MCTargetDesc/MySimulatorInfo.h"
#include "MySimulatorFrameLowering.h"
#include "MySimulatorISelLowering.h"
#include "MySimulatorInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "MySimulatorGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class MySimulatorSubtarget : public MySimulatorGenSubtargetInfo {
  virtual void anchor();

  MySimulatorRegisterInfo RegInfo;
  MySimulatorInstrInfo InstrInfo;
  MySimulatorFrameLowering FrameLowering;
  MySimulatorTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
  MySimulatorABI::ABI TargetABI = MySimulatorABI::ABI_ILP32;
  MVT XLenVT = MVT::i32;
  unsigned XLen = 32;

public:
  MySimulatorSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const MySimulatorInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const MySimulatorFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const MySimulatorTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const MySimulatorRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  auto getTargetABI() const { return TargetABI; }
  auto getXLenVT() const { return XLenVT; }
  auto getXLen() const { return XLen; }
};

} // end namespace llvm

#endif // __LLVM_LIB_TARGET_MY_SIMULATOR_MY_SIMULATOR_SUBTARGET_H__
